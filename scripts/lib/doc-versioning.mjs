#!/usr/bin/env node

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");

export const BROWSER_LIFECYCLE_DOCS_ROOT = path.join(
  rootDir,
  "apps/docs/docs/packages/browser-lifecycle",
);
export const BROWSER_LIFECYCLE_ARCHIVES_ROOT = path.join(
  rootDir,
  "apps/docs/archives/browser-lifecycle",
);
export const BROWSER_LIFECYCLE_VERSIONS_MANIFEST = path.join(
  rootDir,
  "apps/docs/doc-versions/browser-lifecycle.json",
);
export const BROWSER_LIFECYCLE_VERSIONS_META = path.join(
  rootDir,
  "apps/docs/docs/.vitepress/browser-lifecycle-versions.ts",
);
export const BROWSER_LIFECYCLE_PACKAGE_JSON = path.join(
  rootDir,
  "packages/browser-lifecycle/package.json",
);

export function readJson(filePath) {
  return JSON.parse(readFileSync(filePath, "utf8"));
}

export function readBrowserLifecyclePackageVersion() {
  const pkg = readJson(BROWSER_LIFECYCLE_PACKAGE_JSON);
  return pkg.version;
}

export function readBrowserLifecycleVersionsManifest() {
  return readJson(BROWSER_LIFECYCLE_VERSIONS_MANIFEST);
}

export function writeBrowserLifecycleVersionsManifest(manifest) {
  writeFileSync(
    BROWSER_LIFECYCLE_VERSIONS_MANIFEST,
    `${JSON.stringify(manifest, null, 2)}\n`,
    "utf8",
  );
}

export function archiveDirectoryName(version) {
  // Keep in sync with scripts/doc-versioning-policy.ts
  return `v${version}`;
}

export function archiveBasePath(version) {
  // Keep in sync with scripts/doc-versioning-policy.ts
  return `/packages/browser-lifecycle/${archiveDirectoryName(version)}`;
}

export function shouldArchiveForBumpType(currentVersion, bumpType, archivePolicy = "minor") {
  // Keep in sync with scripts/doc-versioning-policy.ts
  if (!bumpType || bumpType === "none") {
    return false;
  }

  if (bumpType === "major") {
    return true;
  }

  const major = Number.parseInt(currentVersion.split(".")[0] ?? "0", 10);

  if (archivePolicy === "major") {
    return bumpType === "major";
  }

  if (major === 0) {
    return bumpType === "minor" || bumpType === "major";
  }

  return bumpType === "major";
}

export function readPendingBrowserLifecycleBumpType() {
  const changesetDir = path.join(rootDir, ".changeset");
  if (!existsSync(changesetDir)) {
    return null;
  }

  const files = readdirSync(changesetDir).filter(
    (file) => file.endsWith(".md") && file !== "README.md",
  );

  let bumpType = null;

  for (const file of files) {
    const content = readFileSync(path.join(changesetDir, file), "utf8");
    const match = content.match(
      /["']@jayoncode\/browser-lifecycle["']\s*:\s*(patch|minor|major)/,
    );

    if (!match) {
      continue;
    }

    const nextBump = match[1];
    if (!bumpType) {
      bumpType = nextBump;
      continue;
    }

    const rank = { patch: 0, minor: 1, major: 2 };
    if (rank[nextBump] > rank[bumpType]) {
      bumpType = nextBump;
    }
  }

  return bumpType;
}

export function syncBrowserLifecycleVersionsMeta() {
  const manifest = readBrowserLifecycleVersionsManifest();
  const currentVersion = readBrowserLifecyclePackageVersion();
  const archives = [...(manifest.archives ?? [])].sort((left, right) =>
    right.version.localeCompare(left.version, undefined, { numeric: true }),
  );

  const body = `/** Generated by scripts/lib/doc-versioning.mjs — do not edit manually. */

export const browserLifecycleDocVersions = {
  packageName: "${manifest.package}",
  basePath: "${manifest.basePath}",
  archivePolicy: "${manifest.archivePolicy ?? "minor"}",
  currentVersion: "${currentVersion}",
  currentVersionLabel: "v${currentVersion}",
  archives: ${JSON.stringify(archives, null, 2)},
} as const;

export type BrowserLifecycleDocArchive = (typeof browserLifecycleDocVersions.archives)[number];
`;

  writeFileSync(BROWSER_LIFECYCLE_VERSIONS_META, body, "utf8");
  return { currentVersion, archiveCount: archives.length };
}
